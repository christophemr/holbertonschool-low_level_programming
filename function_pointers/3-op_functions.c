#include "3-calc.h"

/**
 * op_add - add integers
 * @a: integer to add to b
 * @b: integer to add to a
 * Return: sum of a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract integers
 * @a: integer b is sub from
 * @b: integer to subtract from a
 * Return: result of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply numbers
 * @a: integer 1
 * @b: integer 2
 * Return: result of a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide digits
 * @a: integer to divide
 * @b: second integer
 * Return: result of a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - mod integers
 * @a: first integer
 * @b: second integer
 * Return: result of a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
